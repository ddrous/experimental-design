"""
flow_models.py
===============
Model definitions for solving the source-localisation Bayesian Experimental Design (BED)
problem with *simulation-free Conditional Flow Matching* (CFM), extending the joint
policy + vector-field idea from the reference PDF.

Framing (see main.py docstring for the full derivation):

    We treat theta as being transported by a Continuous Normalizing Flow from a base
    point z0 ~ p(theta) (the *prior*) to a target point z1 = theta_true, where theta_true
    is the value that actually generated the (adaptively-chosen) history h_T = {(x_t, y_t)}.
    Because h_T was generated by simulating y_t ~ p(y | x_t, theta_true), theta_true is a
    valid sample of the posterior p(theta | h_T) induced by that particular sequence of
    designs. Training regresses a vector field v_theta(z_t, t, x_t, y_t) against the
    analytic velocity of a (possibly non-linear) interpolation path between z0 and z1,
    while a design-policy network pi_phi(z_t, t) -> x_t is trained *jointly*, end-to-end,
    through the same loss (the policy never sees z1 directly, so there is no "identity
    trap" as described in the PDF: the only way for the policy to reduce the loss is to
    pick designs x_t that make the resulting y_t informative about z1).

    We do NOT track an explicit Gaussian belief (mu, log_sigma) -- only a single point
    z_t per particle ("point" option). At generation time we integrate an ODE (Euler /
    Midpoint / diffrax Dopri5) from t=0 to t=1, sharing one realised design/outcome
    sequence across an ensemble of particles (each with an independent z0) to obtain
    approximate posterior samples theta | h_T at t=1, and to visualise an EIG-like proxy
    from how quickly the ensemble contracts.
"""
