# ## 4. Training loss (simulation-free flow matching, Algorithm 1 of the PDF, adapted)

# %%
def make_loss_fn(cfg: Config, sim_cfg: SourceLocConfig):
    path_fn = get_path_fn(cfg.path_type, power=cfg.path_power)

    def per_example_loss(model: FlowBEDModel, theta_true: jnp.ndarray, key):
        k_z0, k_t, k_env = jax.random.split(key, 3)

        theta_true_sorted = sort_sources_by_amplitude(theta_true, sim_cfg)  # canonicalise
        z1 = theta_true_sorted.reshape(-1)
        z0 = cfg.prior_std * jax.random.normal(k_z0, (cfg.theta_dim,))
        t = jax.random.uniform(k_t, ())

        z_t, u_t = path_fn(z0, z1, t)

        x_t = model.policy(z_t, t)
        y_t = environment_step(theta_true_sorted, x_t, k_env, sim_cfg)

        v_hat = model.vector_field(z_t, t, x_t, y_t)
        flow_mse = jnp.mean((v_hat - u_t) ** 2)

        # ## Let's permute the velocities u_t, and the minimum MSE over all permutations is the loss. This is permutation-invariant, since the sources are unordered.
        # perm_indices = jnp.array(list(permutations(range(cfg.k_sources)))) #Shape: (K!, K)
        # for perm in perm_indices:
        #     perm = jnp.array(perm)
        #     perm = jnp.repeat(perm, 2) #Shape: (2K,)
        #     u_t_perm = u_t[perm]
        #     flow_mse_perm = jnp.mean((v_hat - u_t_perm) ** 2)
        #     if 'flow_mse' not in locals():
        #         flow_mse = flow_mse_perm
        #     else:
        #         flow_mse = jnp.minimum(flow_mse, flow_mse_perm)

        reg = design_regularizer(x_t, kind=cfg.design_reg)
        loss = flow_mse + cfg.design_reg_weight * reg
        return loss, flow_mse

    @eqx.filter_value_and_grad(has_aux=True)
    def loss_fn(model: FlowBEDModel, theta_batch: jnp.ndarray, key):
        keys = jax.random.split(key, theta_batch.shape[0])
        losses, flow_mses = jax.vmap(per_example_loss, in_axes=(None, 0, 0))(model, theta_batch, keys)
        return jnp.mean(losses), {"flow_mse": jnp.mean(flow_mses)}

    return loss_fn


def make_train_step(cfg: Config, sim_cfg: SourceLocConfig, optimizer: optax.GradientTransformation):
    loss_fn = make_loss_fn(cfg, sim_cfg)

    @eqx.filter_jit
    def train_step(model, opt_state, theta_batch, key):
        (loss, aux), grads = loss_fn(model, theta_batch, key)
        updates, opt_state = optimizer.update(grads, opt_state, model)
        model = eqx.apply_updates(model, updates)
        return model, opt_state, loss, aux

    return train_step


# %% [markdown]
# ## 5. Training loop
#
# `data_mode="finite"` (default): one epoch = one full, seeded-shuffle pass over the
# fixed pool of `cfg.n_train_episodes` pre-seeded episodes (`len(train_loader)` batches).
# `data_mode="infinite"`: one epoch = `cfg.steps_per_epoch` freshly streamed batches.

# %%
def train(cfg: Config, sim_cfg: SourceLocConfig, model: FlowBEDModel, key, run_dir: Path):
    prior = SourceLocPrior(K=cfg.k_sources, prior_std=cfg.prior_std)
    train_loader = make_train_loader(prior, cfg.batch_size, base_seed=cfg.seed,
                                      data_mode=cfg.data_mode, n_train_episodes=cfg.n_train_episodes)

    optimizer = optax.adam(cfg.lr)
    opt_state = optimizer.init(eqx.filter(model, eqx.is_array))
    train_step = make_train_step(cfg, sim_cfg, optimizer)

    epoch_losses, step_losses, step_flow_mses = [], [], []
    infinite_iter = iter(train_loader) if cfg.data_mode == "infinite" else None

    print("Starting training...")
    for epoch in range(cfg.epochs):
        if cfg.data_mode == "finite":
            batch_source = train_loader          # full, reproducible pass = one epoch
            n_batches = len(train_loader)
        else:
            n_batches = cfg.steps_per_epoch
            batch_source = (next(infinite_iter) for _ in range(n_batches))

        running, n_steps = 0.0, 0
        pbar = tqdm(batch_source, total=n_batches, desc=f"Epoch {epoch + 1}/{cfg.epochs}")
        for theta_batch_np in pbar:
            key, batch_key = jax.random.split(key)
            theta_batch = jnp.asarray(theta_batch_np)
            model, opt_state, loss_val, aux = train_step(model, opt_state, theta_batch, batch_key)
            loss_f = float(loss_val)
            running += loss_f
            n_steps += 1
            step_losses.append(loss_f)
            step_flow_mses.append(float(aux["flow_mse"]))
            pbar.set_postfix(loss=f"{loss_f:.4f}")

        avg = running / max(n_steps, 1)
        epoch_losses.append(avg)
        print(f"Epoch {epoch + 1} average loss: {avg:.4f} ({n_steps} steps)")

        # checkpoint every epoch so partial progress is never lost
        save_model(model, run_dir / "checkpoint.eqx")

    history = {"epoch_losses": epoch_losses, "step_losses": step_losses, "step_flow_mses": step_flow_mses}
    with open(run_dir / "train_history.json", "w") as f:
        json.dump(history, f)
    return model, history, key


