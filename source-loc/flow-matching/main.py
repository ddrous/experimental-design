# %% [markdown]
# ## 7. Evaluation
#
# For each held-out episode we roll out an ensemble of particles (see module docstring)
# under `cfg.solver`, and score the ensemble-mean at `t=1` against `theta_true` using the
# best-permutation MSE (permutation-invariant since sources are unordered).

# %%
def best_permutation_mse(pred: np.ndarray, true: np.ndarray, K: int) -> float:
    pred = pred.reshape(K, 2)
    true = true.reshape(K, 2)
    best = np.inf
    for perm in permutations(range(K)):
        mse = float(np.mean((pred[list(perm)] - true) ** 2))
        best = min(best, mse)
    return best


def evaluate(cfg: Config, sim_cfg: SourceLocConfig, model: FlowBEDModel, key) -> dict:
    prior = SourceLocPrior(K=cfg.k_sources, prior_std=cfg.prior_std)
    eval_loader = make_eval_loader(prior, n_episodes=cfg.n_eval_episodes,
                                    batch_size=cfg.n_eval_episodes, seed=cfg.seed + 1)
    eval_theta = jnp.asarray(next(iter(eval_loader)))  # (N, K, 2)

    per_episode_mse = []
    rollouts = []
    for i in tqdm(range(eval_theta.shape[0]), desc="Evaluating"):
        key, k = jax.random.split(key)
        theta_true = eval_theta[i]
        theta_true_sorted = sort_sources_by_amplitude(theta_true, sim_cfg)
        result = run_rollout(cfg.solver, model, theta_true_sorted, k, sim_cfg,
                              cfg.num_particles, cfg.num_design_steps, cfg.theta_dim, cfg.prior_std)
        posterior_mean = np.asarray(result.z_ensemble[-1].mean(axis=0))
        mse = best_permutation_mse(posterior_mean, np.asarray(theta_true_sorted.reshape(-1)), cfg.k_sources)
        per_episode_mse.append(mse)
        if i < cfg.n_viz_episodes:
            rollouts.append((theta_true_sorted, result))

    metrics = {
        "mean_posterior_mse": float(np.mean(per_episode_mse)),
        "median_posterior_mse": float(np.median(per_episode_mse)),
        "solver": cfg.solver,
        "num_design_steps": cfg.num_design_steps,
        "num_particles": cfg.num_particles,
    }
    return {"metrics": metrics, "per_episode_mse": per_episode_mse, "rollouts": rollouts}


