def plot_trajectory(theta_true_sorted: jnp.ndarray, result: RolloutResult, cfg: Config, tag: str):
    theta_true = np.asarray(theta_true_sorted).reshape(cfg.k_sources, 2)
    z_ensemble = np.asarray(result.z_ensemble)                          # (T+1, M, D)
    mean_path = z_ensemble.mean(axis=1).reshape(-1, cfg.k_sources, 2)   # (T+1, K, 2)
