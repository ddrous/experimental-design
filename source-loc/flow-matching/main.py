# ### Source localisation, run as a cell-by-cell notebook (VS Code / Jupyter `# %%` cells)
#
# --------------------------------------------------------------------------------------
# ### Why flow matching, and how it differs from the reference PDF
# --------------------------------------------------------------------------------------
# The reference PDF ("Joint Training of Policies and Continuous Normalizing Flows via
# Conditional Flow Matching") sketches a policy `pi_phi(z_t, z)` that observes the
# *final* target `z` and progressively reveals information about it to a vector field
# `v_theta`. That formulation is too simple for BED: in a real experiment-design loop,
# nothing ever observes the true parameter theta directly -- the policy can only choose
# a design `x_t`, and the environment returns a noisy outcome `y_t = p(y | x_t, theta_true)`.
#
# We adapt the recipe as follows:
#
#   * The **base distribution** of the flow is the experiment's prior `p(theta)` itself
#     (not a generic `N(0, I)`): `z0 ~ p(theta)`.
#   * The **target** `z1` is `theta_true`, the parameter that actually generated the
#     episode. Because every `y_t` along the episode was generated by the *true*
#     simulator conditioned on `theta_true` (never resampled from a model), `theta_true`
#     is, by construction, a valid draw from the posterior `p(theta | h_T)` induced by
#     that particular sequence of designs/outcomes `h_T = {(x_t, y_t)}`. Training the
#     flow to map `z0 -> theta_true` is therefore exactly amortised posterior sampling,
#     in the spirit of (sequential) neural posterior estimation / SBI.
#   * The **policy** `pi_phi(z_t, t) -> x_t` only ever sees the *current* flow state
#     `z_t` and the flow-time `t` -- never `z1` -- so there is no "Identity Trap": the
#     only way the policy can lower the joint flow-matching loss is by choosing designs
#     whose resulting (real, differentiable) outcome `y_t` actually helps the vector
#     field predict the correct velocity. Gradients flow `loss -> v_theta -> x_t -> pi_phi`
#     end-to-end.
#   * Training is **simulation-free**, exactly in the sense of the PDF's Algorithm 1: for
#     each training example we sample a single random flow-time `t ~ U(0,1)`, evaluate
#     the interpolated state `z_t` analytically along a chosen probability path (not
#     necessarily the straight OT line -- see `flow_models.get_path_fn`), and regress the
#     vector field against the path's analytic velocity. No ODE solve is needed at train
#     time.
#   * We use the **"point" representation only** (no explicit Gaussian belief /
#     log-sigma): each particle is transported as a single point, matching the request
#     that we move one sample from the prior to one sample of the posterior.
#   * At **generation time** (evaluation/visualisation) we integrate an ODE from `t=0` to
#     `t=1`. Three solvers are supported: `'euler'`, `'midpoint'` (hand-rolled via
#     `jax.lax.scan`), and `'dopri5'` (adaptive Runge-Kutta via `diffrax`). To visualise
#     an EIG-like quantity without an explicit belief, we integrate an *ensemble* of
#     particles (independent `z0` draws) that all share the *same* realised
#     design/outcome sequence (the design at each flow-time is computed from the
#     ensemble mean, since only one design can physically be queried at a time) and track
#     how the ensemble's spread contracts over flow-time -- see `flow_models.eig_per_step`.
#
# --------------------------------------------------------------------------------------
# ### Reproducibility
# --------------------------------------------------------------------------------------
# * `cfg.data_mode = "finite"` (default) pre-generates the *entire* fixed pool of
#   `cfg.n_train_episodes` seeds up front (see `datalloaders.FiniteEpisodes`); one epoch
#   is then a genuine, seeded-shuffle traversal of that whole pool, not an arbitrary
#   number of steps. `cfg.steps_per_epoch` is therefore only used in `data_mode="infinite"`.
# * Everything downstream (model init, per-step batch keys, environment noise) is a
#   deterministic function of `cfg.seed`, so two runs with the same `cfg` (and the same
#   XLA/hardware backend) produce identical results.
# * On GPU, a handful of JAX ops are not bit-exactly deterministic by default; set
#   `os.environ["XLA_FLAGS"] = "--xla_gpu_deterministic_ops=true"` before importing jax
#   if you need bit-exact reproducibility there too. CPU execution is deterministic as-is.
#
# --------------------------------------------------------------------------------------
