def make_run_dir(base: Path = THIS_DIR / "runs") -> Path:
    stamp = datetime.now().strftime("%Y-%m-%d_%H-%M-%S")
    run_dir = base / stamp
    (run_dir / "plots").mkdir(parents=True, exist_ok=True)
    return run_dir


def snapshot_code(run_dir: Path) -> None:
    """Copy this script and its sibling modules into the run dir for provenance, so the
    run is fully reproducible (and re-running `main.py` *from inside* the run dir with
    `cfg.train = False` picks up its own checkpoint automatically)."""
    for name in ("main.py", "flow_models.py", "models.py", "datalloaders.py"):
        src = THIS_DIR / name
        if src.exists():
            shutil.copy2(src, run_dir / name)


def find_checkpoint(cfg: Config) -> Path:
    candidates = []
    if cfg.checkpoint:
        candidates.append(Path(cfg.checkpoint))
    if cfg.run_dir:
        candidates.append(Path(cfg.run_dir) / "checkpoint.eqx")
    # running from a copied run folder: checkpoint sits right next to this script
    candidates.append(THIS_DIR / "checkpoint.eqx")
    # fall back: most recent run under runs/
    runs_base = THIS_DIR / "runs"
    if runs_base.exists():
        runs = sorted([d for d in runs_base.iterdir() if d.is_dir()], reverse=True)
        candidates.extend([d / "checkpoint.eqx" for d in runs])
    for c in candidates:
        if c.exists():
            return c
    raise FileNotFoundError(
        "No checkpoint found. Set cfg.checkpoint or cfg.run_dir explicitly, or train a "
        "model first with cfg.train = True."
    )


def save_config(cfg: Config, run_dir: Path) -> None:
    with open(run_dir / "config.json", "w") as f:
        json.dump(asdict(cfg), f, indent=2)


def load_config(run_dir: Path) -> Config:
    with open(Path(run_dir) / "config.json") as f:
        d = json.load(f)
    loaded = Config()
    for k, v in d.items():
        if hasattr(loaded, k):
            setattr(loaded, k, v)
    return loaded


def save_model(model: FlowBEDModel, path: Path) -> None:
    eqx.tree_serialise_leaves(str(path), model)


def load_model(cfg: Config, path: Path, key) -> FlowBEDModel:
    skeleton = build_model(cfg.theta_dim, cfg.design_dim, cfg.obs_dim, key=key,
                            policy_hidden=cfg.policy_hidden, policy_depth=cfg.policy_depth,
                            vf_hidden=cfg.vf_hidden, vf_depth=cfg.vf_depth,
                            time_embed_dim=cfg.time_embed_dim, design_bound=cfg.design_bound)
    return eqx.tree_deserialise_leaves(str(path), skeleton)


